export function getProgressionMessage(percentage: number): string {
    let key = "0";
    if (percentage === 0) key = "0";
    else if (percentage < 5) key = "1-4";
    else if (percentage < 10) key = "5-9";
    else if (percentage < 15) key = "10-14";
    else if (percentage < 20) key = "15-19";
    else if (percentage < 25) key = "20-24";
    else if (percentage < 30) key = "25-29";
    else if (percentage < 35) key = "30-34";
    else if (percentage < 40) key = "35-39";
    else if (percentage < 45) key = "40-44";
    else if (percentage < 50) key = "45-49";
    else if (percentage < 55) key = "50-54";
    else if (percentage < 60) key = "55-59";
    else if (percentage < 65) key = "60-64";
    else if (percentage < 70) key = "65-69";
    else if (percentage < 75) key = "70-74";
    else if (percentage < 80) key = "75-79";
    else if (percentage < 85) key = "80-84";
    else if (percentage < 90) key = "85-89";
    else if (percentage < 95) key = "90-94";
    else if (percentage < 99) key = "95-98";
    else if (percentage < 100) key = "99";
    else if (percentage === 100) key = "100";
    else if (percentage < 110) key = "101-109";
    else if (percentage < 125) key = "110-124";
    else if (percentage < 150) key = "125-149";
    else if (percentage < 175) key = "150-174";
    else if (percentage < 200) key = "175-199";
    else if (percentage < 250) key = "200-249";
    else if (percentage < 300) key = "250-299";
    else if (percentage < 350) key = "300-349";
    else if (percentage < 400) key = "350-399";
    else if (percentage < 450) key = "400-449";
    else if (percentage < 500) key = "450-499";
    else if (percentage < 600) key = "500-599";
    else if (percentage < 700) key = "600-699";
    else if (percentage < 800) key = "700-799";
    else if (percentage < 900) key = "800-899";
    else if (percentage < 1000) key = "900-999";
    else key = "1000";

    const possibleMessages = PROGRESSION_MESSAGES[key] || ["Continue l’effort !"];
    return possibleMessages[Math.floor(Math.random() * possibleMessages.length)];
}
